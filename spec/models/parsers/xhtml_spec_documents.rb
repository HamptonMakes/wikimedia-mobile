<p><b>Merb</b> is a <a href="/wiki/Model_View_Controller" title="Model View Controller" class="mw-redirect">Model View Controller</a> <a href="/wiki/Web_framework" title="Web framework" class="mw-redirect">web framework</a> written in <a href="/wiki/Ruby_(programming_language)" title="Ruby (programming language)">Ruby</a>. Merb adopts an approach that focuses on essential core functionality, leaving most functionality to <a href="/wiki/Plugin" title="Plugin" class="mw-redirect">plugins</a>.</p>
<p><a name="Overview" id="Overview"></a></p>
<h2><span class="editsection">[<a href="/w/index.php?title=Merb&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a>]</span> <button class='section_heading show' section_id='1'>Show</button><button class='section_heading hide' style='display: none' section_id='1'>Hide</button> <span>Overview</span></h2><div style='display:none' class='content_block' id='content_1'>

<p>Merb allows for developer preference in choice of <a href="/wiki/Object-relational_mapping" title="Object-relational mapping">ORM</a> (<a href="/wiki/ActiveRecord_(Rails)" title="ActiveRecord (Rails)">ActiveRecord</a>, <a href="/wiki/Datamapper" title="Datamapper">Datamapper</a>, and <a href="/wiki/Sequel" title="Sequel">Sequel</a> (<a href="/wiki/ORM" title="ORM" class="mw-redirect">ORM</a> library) are supported), <a href="/wiki/JavaScript" title="JavaScript">JavaScript</a> library, and <a href="/wiki/Template_engine_(web)" title="Template engine (web)">template language</a> (Support for <a href="/wiki/Haml" title="Haml">HAML</a> is included in addition to <a href="/wiki/ERuby" title="ERuby">eRuby</a>).</p>

<p>Starting February 2008, Merb underwent a large rewrite, jumping from version 0.5 to 0.9.<sup id="cite_ref-0" class="reference"><a href="#cite_note-0" title=""><span>[</span>1<span>]</span></a></sup> Core framework functionality was modularized into <a href="/w/index.php?title=Merb-core&amp;action=edit&amp;redlink=1" class="new" title="Merb-core (page does not exist)">merb-core</a>, and all extra functionality (e.g. various templating language plugins) inside a <a href="/w/index.php?title=Helper_tools&amp;action=edit&amp;redlink=1" class="new" title="Helper tools (page does not exist)">merb-more</a> and <a href="/w/index.php?title=Merb-plugins&amp;action=edit&amp;redlink=1" class="new" title="Merb-plugins (page does not exist)">merb-plugins</a> components. One can still create simple web applications with merb-core alone, similar to the <a href="/wiki/Camping_(microframework)" title="Camping (microframework)">Camping</a> microframework.</p>

<p>Merb 1.0 was released on November 7 2008 at <a href="/w/index.php?title=RubyConf&amp;action=edit&amp;redlink=1" class="new" title="RubyConf (page does not exist)">RubyConf</a>.<sup id="cite_ref-1" class="reference"><a href="#cite_note-1" title=""><span>[</span>2<span>]</span></a></sup> and is currently at version 1.0.6.1.</p>
<p>Merb was highly influenced by <a href="/wiki/Ruby_on_Rails" title="Ruby on Rails">Rails</a>, and it can also be used to write sophisticated applications and <a href="/wiki/Representational_State_Transfer" title="Representational State Transfer">REST</a> Web services. Merb being more flexible and faster <sup id="cite_ref-2" class="reference"><a href="#cite_note-2" title=""><span>[</span>3<span>]</span></a></sup> than Rails, it's supposed to be the "smart" choice for people having to deal with <a href="/wiki/Legacy" title="Legacy">legacy</a> applications/databases and/or huge traffic.<sup class="noprint Template-Fact"><span title="This claim needs references to reliable sources&#160;since October 2008" style="white-space: nowrap;">[<i><a href="/wiki/Wikipedia:Citation_needed" title="Wikipedia:Citation needed">citation needed</a></i>]</span></sup></p>

<p>As of December 23, 2008, the Merb project is being merged with Rails, and a version with the best features of both will be released in 2009 under the name Rails 3<sup id="cite_ref-3" class="reference"><a href="#cite_note-3" title=""><span>[</span>4<span>]</span></a></sup><sup id="cite_ref-4" class="reference"><a href="#cite_note-4" title=""><span>[</span>5<span>]</span></a></sup>.</p>
